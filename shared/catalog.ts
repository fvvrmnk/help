import { slugifyRu } from "./slugify";

export type CountryKey = "rf" | "rb" | "ua";

export type ServiceGroup =
  | "Справки"
  | "ЗАГС"
  | "Права"
  | "Паспорта"
  | "Образование";

export const countries: Array<{ key: CountryKey; nameRu: string; nameRuGenitive: string }> = [
  { key: "rf", nameRu: "Российская Федерация", nameRuGenitive: "Российской Федерации" },
  { key: "rb", nameRu: "Республика Беларусь", nameRuGenitive: "Республики Беларусь" },
  { key: "ua", nameRu: "Украина", nameRuGenitive: "Украины" },
];

export function getCountryNameRu(key: CountryKey) {
  return countries.find((c) => c.key === key)?.nameRu ?? key;
}

export function getCountryNameRuGenitive(key: CountryKey) {
  return countries.find((c) => c.key === key)?.nameRuGenitive ?? key;
}

export interface ServiceItem {
  id: string;
  countryKey: CountryKey;
  serviceGroup: ServiceGroup;
  nameRu: string;
  slug: string;
  shortSummary: string;
  priceLabel: "По запросу";
}

function svc(
  countryKey: CountryKey,
  serviceGroup: ServiceGroup,
  nameRu: string,
  shortSummary: string,
): ServiceItem {
  const slug = slugifyRu(nameRu);
  return {
    id: `${countryKey}:${slug}`,
    countryKey,
    serviceGroup,
    nameRu,
    slug,
    shortSummary,
    priceLabel: "По запросу",
  };
}

